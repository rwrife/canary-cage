runner = CliRunner()


def _ctx(**overrides) -> FireContext:
    defaults = dict(canary_id="abc123", canary_type="markdown", path="README.md")
    defaults.update(overrides)
    return FireContext(**defaults)


def test_clear_single_agent_match_claude():
    """A commit signed by Claude Code should rank claude-code first with
    multiple signals."""
    ctx = _ctx(
        commit_message=(
            "Refactor scanner module\n\n"
            "🤖 Generated with [Claude Code](https://claude.com/code)\n"
            "Co-Authored-By: Claude <noreply@anthropic.com>"
        ),
        commit_author="Claude <noreply@anthropic.com>",
    )
    report = Fingerprinter().identify(ctx)
    assert report.top is not None
    assert report.top.agent == "claude-code"
    assert report.top.confidence > 0.5
    # At least a few rules should have matched.
    assert len(report.top.signals) >= 3


def test_clear_single_agent_match_copilot_via_user_agent():
    ctx = _ctx(user_agent="github-copilot-agent/1.2.0", commit_author="copilot[bot]")
    report = Fingerprinter().identify(ctx)
    assert report.top is not None
    assert report.top.agent == "github-copilot"
    assert any("user_agent" in s or "copilot" in s for s in report.top.signals)


def test_ambiguous_multi_candidate_ranking():
    """A commit that pings both Cursor- and generic-LLM rules should
    return multiple ranked candidates with Cursor on top (heavier weight)."""
    ctx = _ctx(
        commit_message="[cursor] ✨ ai-generated refactor",
        commit_author="cursor[bot]",
    )
    report = Fingerprinter().identify(ctx)
    agents = [c.agent for c in report.candidates]
    assert "cursor" in agents
    assert "generic-llm" in agents
    # Cursor should outrank generic-llm.
    assert agents.index("cursor") < agents.index("generic-llm")


def test_no_match_returns_empty_candidates():
    ctx = _ctx(
        commit_message="fix: typo in README",
        commit_author="Alice <alice@example.com>",
    )
    report = Fingerprinter().identify(ctx)
    assert report.candidates == []
    assert report.top is None
    # to_dict must still produce a stable shape.
    d = report.to_dict()
    assert d == {"top": None, "candidates": []}


def test_to_dict_schema_stability():
    ctx = _ctx(
        commit_message="Generated by Cursor agent",
        commit_author="cursoragent",
    )
    report = Fingerprinter().identify(ctx)
    payload = report.to_dict()
    # Required keys.
    assert set(payload.keys()) == {"top", "candidates"}
    assert set(payload["top"].keys()) == {"agent", "confidence"}
    for cand in payload["candidates"]:
        assert set(cand.keys()) == {"agent", "display", "confidence", "signals"}
        assert isinstance(cand["confidence"], float)
        assert 0.0 <= cand["confidence"] <= 1.0


