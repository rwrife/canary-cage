def test_each_agent_has_at_least_four_rules():
    """Acceptance: 4+ distinct heuristic rules per agent class."""
    fp = Fingerprinter()
    for agent in fp.pack["agents"]:
        assert (
            len(agent["rules"]) >= 4
        ), f"{agent['agent']} has only {len(agent['rules'])} rules"


