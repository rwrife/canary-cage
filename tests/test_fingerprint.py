def test_check_json_includes_attributed_to(tmp_path: Path):
    _init_git_repo(
        tmp_path,
        author_name="Claude",
        author_email="noreply@anthropic.com",
        commit_msg="docs: tweak\n\n🤖 Generated with [Claude Code]",
        file_rel="README.md",
        content="hello",
    )
    canary = PlantedCanary(id="c1", type="markdown", path="README.md", marker="m1")
    save_state(tmp_path, CageState(canaries=[canary]))

    result = runner.invoke(app, ["check", "--root", str(tmp_path), "--json"])
    # Fires were detected → exit 1, but stdout still has JSON.
    assert result.exit_code in (0, 1)
    payload = json.loads(result.stdout)
    assert "fires" in payload
    fires = payload["fires"]
    assert fires, "expected at least one fire"
    # Working-tree fire on c1 should be attributed to claude-code.
    f = next(f for f in fires if f["canary_id"] == "c1")
    assert f["attributed_to"] is not None
    assert f["attributed_to"]["top"]["agent"] == "claude-code"
