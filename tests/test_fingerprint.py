def _init_git_repo(
    path: Path,
    *,
    author_name: str,
    author_email: str,
    commit_msg: str,
    file_rel: str,
    content: str,
):
    subprocess.run(["git", "init", "-q", "-b", "main", str(path)], check=True)
    env = {
        "GIT_AUTHOR_NAME": author_name,
        "GIT_AUTHOR_EMAIL": author_email,
        "GIT_COMMITTER_NAME": author_name,
        "GIT_COMMITTER_EMAIL": author_email,
        "GIT_CONFIG_GLOBAL": "/dev/null",
        "GIT_CONFIG_SYSTEM": "/dev/null",
        "HOME": str(path),
    }
    (path / file_rel).parent.mkdir(parents=True, exist_ok=True)
    (path / file_rel).write_text(content, encoding="utf-8")
    subprocess.run(["git", "-C", str(path), "add", "-A"], check=True, env=env)
    subprocess.run(["git", "-C", str(path), "commit", "-q", "-m", commit_msg], check=True, env=env)


def test_context_from_canary_pulls_git_commit(tmp_path: Path):
    _init_git_repo(
        tmp_path,
        author_name="Claude",
        author_email="noreply@anthropic.com",
        commit_msg="docs: tweak readme\n\n🤖 Generated with [Claude Code]",
        file_rel="README.md",
        content="hello",
    )
    canary = PlantedCanary(id="c1", type="markdown", path="README.md", marker="abcd1234")
    save_state(tmp_path, CageState(canaries=[canary]))

    ctx = context_from_canary(tmp_path, canary)
    assert ctx.commit_message and "Claude" in ctx.commit_message
    assert ctx.commit_author and "anthropic.com" in ctx.commit_author

    report = Fingerprinter(root=tmp_path).identify(ctx)
    assert report.top is not None
    assert report.top.agent == "claude-code"


def test_compute_diff_includes_attributed_to(tmp_path: Path):
    _init_git_repo(
        tmp_path,
        author_name="Cursor Agent",
        author_email="cursoragent@cursor.sh",
        commit_msg="[cursor] tweak",
        file_rel="docs/notes.md",
        content="hello",
    )
    canary = PlantedCanary(
        id="c-removed", type="markdown", path="docs/notes.md", marker="zzz999"
    )
    save_state(tmp_path, CageState(canaries=[canary]))

    report = compute_diff(tmp_path)
    assert len(report.diffs) == 1
    d = report.diffs[0]
    # No BEGIN sentinel in file → REMOVED verdict, which should be attributed.
    assert d.verdict == "removed"
    assert d.attributed_to is not None
    assert d.attributed_to["top"]["agent"] == "cursor"

    # JSON payload exposes the additive field.
    payload = report.to_dict()
    item = payload["groups"][0]["items"][0]
    assert "attributed_to" in item
    assert item["attributed_to"]["top"]["agent"] == "cursor"


